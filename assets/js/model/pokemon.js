class Pokemon {
     constructor(name,id,image,types,vitality,attack,defense,speed,specialAttack,specialDefense,abilities,category,height,weight,species,evolutionChain){
          this.name = name;
          this.id = id;
          this.image = image;
          this.types = types;
          this.vitality = vitality;
          this.attack = attack;
          this.defense = defense;
          this.speed = speed;
          this.specialAttack = specialAttack;
          this.specialDefense = specialDefense;
          this.abilities = abilities;
          this.category = category;
          this.height = height;
          this.weight = weight;
          this.species = species;
          this.evolutionChain = evolutionChain;
     }
}